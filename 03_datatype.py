# Text Type (str)
text = "Hello, World!"
print("Text Type (str):", text)

# Numeric Types (int, float, complex)
integer = 42
floating_point = 3.14159
complex_number = 2 + 3j
print("Numeric Types (int, float, complex):", integer, floating_point, complex_number)

# Sequence Types (list, tuple, range)
my_list = [1, 2, 3, 4, 5]
my_tuple = (10, 20, 30)
my_range = range(5)
print("Sequence Types (list, tuple, range):", my_list, my_tuple, list(my_range))

# Mapping Type (dict)
my_dict = {"name": "Alice", "age": 30}
print("Mapping Type (dict):", my_dict)

# Set Types (set, frozenset)
my_set = {1, 2, 3, 4, 5}
my_frozenset = frozenset({6, 7, 8, 9, 10})
print("Set Types (set, frozenset):", my_set, my_frozenset)

# Boolean Type (bool)
my_bool = True
print("Boolean Type (bool):", my_bool)

# Binary Types (bytes, bytearray, memoryview)
my_bytes = b'binary data'
my_bytearray = bytearray([65, 66, 67])
my_memoryview = memoryview(my_bytearray)
print("Binary Types (bytes, bytearray, memoryview):", my_bytes, my_bytearray, my_memoryview)

# None Type (NoneType)
my_none = None
print("None Type (NoneType):", my_none)
