# Boolean values
is_true = True
is_false = False

# Basic Boolean operations
print("Boolean Values:")
print("is_true:", is_true)
print("is_false:", is_false)

# Logical AND operation
result_and = is_true and is_false
print("\nLogical AND:")
print("is_true and is_false:", result_and)  # Result: False

# Logical OR operation
result_or = is_true or is_false
print("\nLogical OR:")
print("is_true or is_false:", result_or)    # Result: True

# Logical NOT operation
result_not = not is_true
print("\nLogical NOT:")
print("not is_true:", result_not)           # Result: False

# Comparison operators
x = 10
y = 5

# Greater than
greater_than = x > y
print("\nComparison Operators:")
print("x > y:", greater_than)              # Result: True

# Less than
less_than = x < y
print("x < y:", less_than)                # Result: False

# Equal to
equal_to = x == y
print("x == y:", equal_to)                # Result: False

# Not equal to
not_equal_to = x != y
print("x != y:", not_equal_to)            # Result: True

# Greater than or equal to
greater_equal = x >= y
print("x >= y:", greater_equal)           # Result: True

# Less than or equal to
less_equal = x <= y
print("x <= y:", less_equal)             # Result: False
